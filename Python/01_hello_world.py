# 01 - Hello World
# The simplest Python program: print text to the screen.
# No compilation needed — just run: python 01_hello_world.py

print("Hello, World!")

# print() can take multiple values separated by commas
print("My", "name", "is", "Python")

# sep changes the separator (default is a space)
print("one", "two", "three", sep="-")

# end changes the line ending (default is \n)
print("No newline after this", end=" ")
print("<-- same line")
